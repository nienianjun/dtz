        /// <summary>
        /// 获得（iLength）位随机数
        /// </summary>
        /// <param name="iLength"></param>
        /// <returns></returns>
        public static string GetRandom(int iLength)
        {
            int iRV = 1;
            for (int i = 1; i < iLength - 1; i++)
            {
                iRV *= 10;
            }
            Random rd = new Random();
            return rd.Next(iRV, iRV * 10).ToString();
        }
